int	ft_atoi(const char *nptr)
{
	long int	p;
	long int	n;

	while ((*nptr >= 9 && *nptr <= 13) || *nptr == ' ')
		nptr++;
	n = 0;
	if (*nptr == '-' || *nptr == '+')
		if (*nptr++ == '-')
			n = 1 - n;
	p = 0;
	while (*nptr >= '0' && *nptr <= '9')
		p = p * 10 + *nptr++ - '0';
	if (n)
		p *= -1;
	return (p);
}
