#include"libft.h"

char	*ft_strdup(const char *s)
{
	char	*p;
	int		i;

	i = ft_strlen(s);
	p = malloc(sizeof(char) * (i + 1));
	if (!p)
		return (0);
	i = 0;
	while (s[i])
	{
		p[i] = s[i];
		i++;
	}
	p[i] = '\0';
	return (p);
}
